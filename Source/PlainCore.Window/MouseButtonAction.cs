namespace PlainCore.Window
{
    /// <summary>
    /// Defines the states a mouse button action can have.
    /// </summary>
    public enum MouseButtonAction
    {
        /// <summary>
        /// Mouse button was released.
        /// </summary>
        Release = 0,

        /// <summary>
        /// Mouse button was pressed.
        /// </summary>
        Press = 1
    }
}
