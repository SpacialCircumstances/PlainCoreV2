namespace PlainCore.Window
{
    /// <summary>
    /// Defines a key action.
    /// </summary>
    public enum KeyAction
    {
        /// <summary>
        /// Key was released.
        /// </summary>
        Released = 0,

        /// <summary>
        /// Key was pressed.
        /// </summary>
        Pressed = 1,

        /// <summary>
        /// Key was repeated.
        /// </summary>
        Repeated = 2
    }
}
